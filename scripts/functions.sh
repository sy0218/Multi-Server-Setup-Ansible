#!/usr/bin/bash

# ----------------------------------------
# INFO 로그
# ----------------------------------------
log_info() {
    echo "[INFO ] [$(date '+%Y-%m-%d %H:%M:%S')] $*"
}

# ----------------------------------------
# ERROR 로그
# ----------------------------------------
log_error() {
    echo "[ERROR] [$(date '+%Y-%m-%d %H:%M:%S')] $*" >&2
}
